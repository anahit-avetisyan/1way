        ], 200);
    }
    public function getProductRate($id)
    {

        $productRating = ProductRating::where('product_id',$id)->avg('rate');

        return response()->json([
            'success' => true,
            'rate' =>$productRating ? $productRating:5
        ], 200);
    }
